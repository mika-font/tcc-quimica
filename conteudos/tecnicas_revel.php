                    <hr>
                </div>
                <p>Consiste na aplicação de uma fina camada de pó sobre o local analisado, o pó irá interagir com os compostos químicos e adere-se à impressão digital devido à água presente entre os compostos, portanto, essa técnica é somente sensível a impressões digitais deixadas recentemente, uma vez que a concentração de água começa a diminuir com o passar do tempo, sendo necessário utilizar outras técnicas caso esteja analisando uma impressão digital antiga.</p>
                <p>Essa tecnica é adequada para superfícies lisas, não rugosas e não absorventes.</p>
                <p>A técnica do pó tem como vantagem seu baixo custo e simplicidade de aplicação, porém as condições climáticas e a composição dos pós podem desfavorecer a aplicação dessa técnica no objeto e intervir em outros exames periciais.</p>
                <p>Existem diferentes composições químicas de pós utilizados, como por exemplo:</p>
                <ul>
                    <li>Pó de Óxido de Ferro (Fe2O3): composto de 50% de óxido de ferro, 25% de resina e 25% de negro-de-fumo (substância de cor negra).</li>
                    <li>Pó de Dióxido de Manganês (MnO2): composto de 45% de dióxido de manganês, 25% de óxido de ferro, 25% de negro-de-fumo e 5% de resina.</li>
                </ul>
                <p>Esses dois pós são chamados de <i>“pó negro”</i>.</p>
                <ul>
                    <li>Pó Óxido de Titânio (TiO2): composto de 60% de dióxido de titânio, 20% de talco e 20% de caulim (argila).</li>
                    <li>Pó Carbonato de Chumbo (PbCO3): composto de 80% de carbonato de chumbo, 15% de goma arábica, 3% de alumínio em pó e 2% de negro-de-fumo.</li>
                </ul>
                <p>Esses dois pós são chamados de <i>“pó branco”</i>.</p>
                <ul>
                    <li>Pó Fluorescente: composto de antraceno (hidrocarboneto) finamente pulverizado.</li>
                </ul>
                <p>Esse exemplo de pó fluorescente necessita da utilização de luz ultravioleta para emitir luz visível, é muito útil para superfícies multicoloridas e com padrões confusos.</p>
                <ul>
                    <li>Pó Magnéticos: útil para revelação de digitais em superfícies como plástico e pele humana.</li>
                </ul>
