                    <p>Essa técnica se baseia na reação do nitrato de prata com os cloretos presentes no suor, formando o cloreto de prata AgCl. Posteriormente, quando exposto à luz, o íon de cloreto (Cl-), presente no AgCl, sofre um processo de redução, sendo convertido em prata metálica (Ag), que possui uma coloração escura, o que permite que a impressão digital seja revelada. Contudo, a impressão deve ser fotografada rapidamente antes que toda superfície escureça.</p>
                    <p>Reação: AgNO3 + XCl → AgCl + XNO3-</p>
                    <p>O Nitrato de Prata é uma técnica útil em superfícies porosas, plásticos e madeira não envernizada, mas é importante alertar que, em vista da volatilidade dos cloretos na impressão, essa técnica não se aplica à revelação de impressões digitais antigas.</p>
