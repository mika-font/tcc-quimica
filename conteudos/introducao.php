                    <p> No último século, a Ciência Forense tornou-se crucial como ferramenta de resolução de crimes e na busca pela verdade nos campos da justiça. Nela, a Química Forense ganha destaque, um ramo da química que se preocupa em auxiliar a investigação forense na procura por respostas verídicas para atender aspectos de interesse judiciário, assim volta-se para a identificação e quantificação de elementos e substâncias encontradas na perícia de um local. </p>
                    <p> A química forense possui áreas voltadas ao preparo de amostras e, posterior, classificação e/ou identificação de elementos ou substâncias presentes em evidências relacionadas a um determinado delito, como Toxicologia, Documentoscopia, Análise de Tintas, Material Residuográfico, Análise de Resíduos de Incêndio, Análise de Vestígios Biológicos e Balística, além de estar intrinsecamente relacionada à outros áreas da ciência como, Psicologia, Biologia, Física, Matemática, Sociologia e Farmacologia, a fim de auxiliar na resolução eficaz de um caso criminal. </p>
