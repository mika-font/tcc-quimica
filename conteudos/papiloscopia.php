                    <img src="../assets/img/arco_angular.png">
                </div>
                <p><b>Arco Bifurcado Interno:</b> Possui linhas que atravessam o desenho da direita para a esquerda.</p>
                <div class="text-center">
                    <img src="../assets/img/arco_bifurcado_interno.png">
                </div>
                <p><b>Arco Bifurcado Externo:</b> Possui linhas que atravessam o desenho da esquerda para a direita.</p>
                <div class="text-center">
                    <img src="../assets/img/arco_bifurcado_externo.png">
                </div>
                <p><b>Arco Bifurcado Duplo:</b> Possui características do arco bifurcado interno e externo.</p>
                <div class="text-center">
                    <img src="../assets/img/arco_bifurcado_duplo.png">
                </div>
                <p><b>Arco Normal:</b> Possui linhas curvas que cruzam o desenho e não possuem interferências.</p>
