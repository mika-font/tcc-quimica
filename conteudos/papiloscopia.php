                <div class="text-center">
                    <img src="../assets/img/arco.png">
                </div>
                <p><b>Subclassificação do Arco:</b> Essa classificação pode ser dividida em 7 tipos diferentes de arcos de acordo com a formação dos sentidos das linhas.</p>
                <p><b>Arco de Presilha Interno:</b> Possui laçadas prejudicadas e não se enquadra à classe Presilha Interna.</p>
                <div class="text-center">
                    <img src="../assets/img/arco_presilha_interno.png">
                </div>
