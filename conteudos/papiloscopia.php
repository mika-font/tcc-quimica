            <div class="col-xl-12">
                <p>Desde a antiguidade o ser humano utiliza suas digitais para legalizar seus documentos, como mostram documentos históricos datados a partir de 300 a.C. que foram encontrados por historiadores chineses e neles constam o uso das linhas papilares para autenticar selos e documentos oficiais.</p>
                <p>Atualmente, utilizamos a papiloscopia para obter um resultado preciso em relação a autoria de um crime, uma vez que seu objeto de estudo são as impressões deixadas por nossas papilas dérmicas presentes em nossa pele.</p>
                <p>Para melhor compreensão, as papilas dérmicas são regiões situadas na superfície da derme, camada intermediária da pele humana, essa região possui elevações e atenuações que favorecem a resistência da pele, além de ocasionar a formação de relevos observáveis na epiderme. </p>
                <p>Retornando ao assunto, o uso dessa ciência facilitou o trabalho dos investigadores e garantiu um resultado verídico, uma vez que tais impressões são únicas e imutáveis em cada indivíduo, permitindo ao perito identificar uma pessoa através de sua impressão. </p>
                <p>No entanto, antigamente esse método era outro, menos preciso, porém amplamente aceito naquela época, meados da segunda metade do século XIX, chamado de Antropometria de Alphonse Bertillon. O método de identificação era caracterizado pela documentação do indivíduo, desde suas medidas fisiológicas quanto sua fotografia frontal e perfil.</p>
                <p>O problema desse método surge quando pessoas extremamente semelhantes são confundidas, como é o caso de Will e Willian West, pessoas de nome, medidas e aparência semelhantes que foram recolhidas para a penitenciária Leavenworth, no Kansas, Estados Unidos, e deixou os policiais impressionados, coisa que incentivou o uso de outros métodos para obtenção e confirmação dos reais responsáveis por um crime.</p>
                <p>A papiloscopia é uma ciência dividida em 3 áreas: datiloscopia, quiroscopia e podoscopia, que se dedicam em analisar, respectivamente, as impressões digitais presentes nas polpas dos dedos das mãos, as impressões deixadas pelas palmas das mãos e as impressões deixadas pelas plantas dos pés.</p>
                <p>Vale ressaltar que a datiloscopia é a área de maior destaque entre as demais devido ao papel mais decisivo na identificação de um indivíduo.</p>
                <p>Em 1902, Juan Vucetich Kovacevich, antropólogo, policial e inventor argentino, elaborou e aplicou um sistema de classificação de impressões digitais, com o objetivo de facilitar a identificação de pessoas e agilizar o processo investigativo.</p>
                <p>Ele classificou uma impressão digital em 4 classes principais:</p>
