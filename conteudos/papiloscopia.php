                    <p>Atualmente, a papiloscopia é uma ciência da criminalística que utiliza impressões papilares deixadas pelas papilas dérmicas presentes em nossa pele para obter um resultado preciso acerca da autoria de um delito. Seu uso facilitou o trabalho dos investigadores e garantiu resultados verídicos em decorrência da análise precisa das impressões digitais, uma vez únicas e imutáveis.</p>
                    <p>Para melhor compreensão, as papilas dérmicas são regiões situadas na superfície da derme, camada intermediária da pele humana, essa região possui elevações e atenuações que favorecem a resistência da pele, além de ocasionar a formação de relevos observáveis na epiderme, presentes na ponta dos dedos, nas palmas das mãos e nas solas dos pés.</p>
                    <p>No entanto, antigamente o método utilizado para a identificação humana era outro, menos preciso, porém amplamente aceito naquela época, meados da segunda metade do século XIX, chamado de Antropometria de Alphonse Bertillon. O método de identificação era caracterizado pela documentação minuciosa do indivíduo que já possuía alguma passagem na polícia, desde suas medidas fisiológicas e identificação quanto sua fotografia frontal e de perfil.</p>
                    <p>O problema desse método surge quando pessoas extremamente semelhantes são confundidas e presas, como o caso de Will e Willian West, dois homens de nome, medidas e aparência semelhantes que foram recolhidos pela penitenciária Leavenworth, no Kansas, Estados Unidos, e deixou os policiais do local impressionados. Tal acontecimento incentivou a busca e aperfeiçoamento de outros métodos mais precisos para obtenção e confirmação dos reais responsáveis por um delito.</p>
